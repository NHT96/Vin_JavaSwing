    public void LoadCBO()
     {
         Connection connection = null;
        PreparedStatement statement = null;
        try {
            connection = Connect.getConnection();

            String sql = "SELECT distinct n.MANV FROM TAIKHOAN t, NHANVIEN n WHERE n.MANV not in (SELECT MANV FROM TAIKHOAN)";
            statement = connection.prepareCall(sql);
            ResultSet resultSet = statement.executeQuery();
            Vector data = new Vector();
            while (resultSet.next()) {
                data.add(resultSet.getString(1));
            }
            DefaultComboBoxModel cmbModel = new DefaultComboBoxModel(data);
            cboMNV.setModel(cmbModel);
        } catch (SQLException e) {
            Logger.getLogger(frmTrangChu.class.getName()).log(Level.SEVERE, null, e);
        } finally {
            if (statement != null) {
                try {
                    statement.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frmTrangChu.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (connection != null) {
                try {
                    connection.close();
                } catch (SQLException e) {
                    Logger.getLogger(frmTrangChu.class.getName()).log(Level.SEVERE, null, e);
                }
            }
        }
     }
