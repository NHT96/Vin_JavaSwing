    private void btnXNTaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXNTaoActionPerformed
        // TODO add your handling code here:
        try
            {
                if (txtIDnv.getText() == "" || txtPass.getText() == "")
                {
                    JOptionPane.showMessageDialog(null,"Vui lòng nhập đầy đủ thông tin tài khoản!");
                    return;
                }

                if (txtIDnv.getText().length() != 10)
                {
                    JOptionPane.showMessageDialog(null,"Phải nhập đủ 10 ký tự cho ID đăng nhập!");
                    return;
                }
                if (txtPass.getText().length() < 6 || txtPass.getText().length() >15)
                {
                    JOptionPane.showMessageDialog(null,"Mật khẩu phải có đồ dài từ 6-15 ký tự!");
                    return;
                }

                TKMod tk = new TKMod();
                if (tk.findTK(txtIDnv.getText()) == true)
                {
                    JOptionPane.showMessageDialog(null,"Tài khoản này đã tồn tại. Vui lòng nhập ID khác!");
                    return;
                }

                if (tk.findTK(txtIDnv.getText()) == false)
                {
                    tk.insert(txtIDnv.getText(), txtPass.getText(), cboMNV.getSelectedItem().toString());
                    JOptionPane.showMessageDialog(null,"Thêm Thành Công!");
                    txtIDnv.setText("");
                    txtPass.setText("");
                }
            }
            catch (Exception ex)
            {
            }
    }//GEN-LAST:event_btnXNTaoActionPerformed

    private void btnXNXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXNXoaActionPerformed
        // TODO add your handling code here:
        try
            {
                if (txtIDx.getText().length() != 10 || txtMNVx.getText().length() != 10)
                {
                    JOptionPane.showMessageDialog(null,"Vui lòng nhập đủ 10 ký tự cho ID và MANV!");
                    return;
                }
                TKMod tk = new TKMod();
                if (tk.findTK(txtIDx.getText()) == false)
                {
                    JOptionPane.showMessageDialog(null,"Tài khoản không tồn tại. Vui lòng nhập ID khác!");
                    return;
                }
                if(txtMNVx.getText().equals(frmDangNhap.mnv))
                {
                    JOptionPane.showMessageDialog(null,"Không thể xóa tài khoản đang sử dụng!");
                    return;
                }
                if (tk.findTK(txtIDx.getText()) == true)
                {
                     int option = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa tài khoản này không?");
                     if (option == 0) {
                         tk.delete(txtIDx.getText(), txtMNVx.getText());
                         JOptionPane.showMessageDialog(null,"Xóa Thành Công!");
                         txtIDnv.setText("");
                         txtPass.setText("");
                     }
                }
            }
            catch (Exception ex)
            {
            }
    }//GEN-LAST:event_btnXNXoaActionPerformed

